#include <iostream>
int main ()
 {
for(int i = 0; i <= 10; i+=2) {
std::cout << i << '\n';
}


// ++ adds one number every time after i      (increases)
// -- subtracts one number each time after i   (decreases)
// i <=     condition the loop keeps running as long as the number is less than or eqaul to i
    return 0;
}